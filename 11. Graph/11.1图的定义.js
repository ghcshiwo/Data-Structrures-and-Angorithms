// 图由边的集合及顶点的集合组成
// 如果一个图的顶点对是有序的，则可以称之为有向图
// 如果图是无序的，则称之为无序图
// 图中的一系列顶点构成路径，路径中所有的顶点都由边连接
// 路径的长度用路径中第一个顶点到最后一个顶点之间边的数量表示
// 由指向自身的顶点组成的路径称为环，环的长度为0
// 圈是至少有一条边的路径，且路径的第一个顶点和最后一个顶点相同
// 无论是有向图还是无向图，只要是没有重复边或重复顶点的圈，就是一个简单圈
// 除了第一个和最后一个顶点以外，路径的其他顶点有重复的圈称为平凡圈
// 如果两个顶点之间有路径，那么这两个顶点就是强连通的，反之亦然
// 如果有向图的所有的顶点都是强连通的，那么这个有向图也是强连通的